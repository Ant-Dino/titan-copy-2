import React from 'react';
interface RowActionButtonProps {
  deleteRow: (row: any) => void; // Function passed as a prop to delete a row
  row: any; // Assuming row is an object, details not provided
}
const RowActionButton: React.FC<RowActionButtonProps> = ({ deleteRow, row }) => {
  const onDeleteClick = () => {
    // Ideally, replace with a proper modal or confirmation dialog
    if(window.confirm('Are you sure to delete?')) {
      deleteRow(row);
    }
  };
  return (
    <div>
      <div className="ui-grid-cell-contents">
        <button
          type="button"
          className="btn btn-danger"
          onClick={onDeleteClick}
        >
          <i className="glyphicon glyphicon-trash"></i>
        </button>
      </div>
    </div>
  );
};
export default RowActionButton;